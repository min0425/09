#include <stdio.h>
#include <stdlib.h>
#define SIZE 5
/* run this program using the console pauser or add your own getch, system("pause") or input loop */

int main(int argc, char *argv[]) {
	int i,average;
	int sum; 
	int grade[SIZE];
	sum=0;
	
	printf("5명의 점수를 입력하세요\n");
	
	for(i=0;i<SIZE;i++)
	{
	
		printf("학생%i의 성적을 입력하세요 :",i+1);
		scanf("%d",&grade[i]);
		sum+=grade[i];
	}
	
	
	average = sum/SIZE;
	printf("성적 평균 : %d\n",average);
	
	return 0;
}
